package jxau.spms.common.po;

public class SubjectInfo {

	private int subjectNo;
	private String subTerm;
	private String studentNo;
	private String tutorNo;
	private String subName;
	private String subSource;
	private String papLocation;
	private String subType;
	private String subPosition;
	private String subBrief;
	private int exameState;
	private String memo;
	
	public int getSubjectNo() {
		return subjectNo;
	}
	public void setSubjectNo(int subjectNo) {
		this.subjectNo = subjectNo;
	}
	public String getSubTerm() {
		return subTerm;
	}
	public void setSubTerm(String subTerm) {
		this.subTerm = subTerm;
	}
	public String getStudentNo() {
		return studentNo;
	}
	public void setStudentNo(String studentNo) {
		this.studentNo = studentNo;
	}
	public String getTutorNo() {
		return tutorNo;
	}
	public void setTutorNo(String tutorNo) {
		this.tutorNo = tutorNo;
	}
	public String getSubName() {
		return subName;
	}
	public void setSubName(String subName) {
		this.subName = subName;
	}
	public String getSubSource() {
		return subSource;
	}
	public void setSubSource(String subSource) {
		this.subSource = subSource;
	}
	public String getPapLocation() {
		return papLocation;
	}
	public void setPapLocation(String papLocation) {
		this.papLocation = papLocation;
	}
	public String getSubType() {
		return subType;
	}
	public void setSubType(String subType) {
		this.subType = subType;
	}
	public String getSubPosition() {
		return subPosition;
	}
	public void setSubPosition(String subPosition) {
		this.subPosition = subPosition;
	}
	public String getSubBrief() {
		return subBrief;
	}
	public void setSubBrief(String subBrief) {
		this.subBrief = subBrief;
	}
	public int getExameState() {
		return exameState;
	}
	public void setExameState(int exameState) {
		this.exameState = exameState;
	}
	public String getMemo() {
		return memo;
	}
	public void setMemo(String memo) {
		this.memo = memo;
	}
	
}
