package jxau.spms.common.po;

public class TermInfo {

	private int termNo;
	private String term;
	
	public int getTermNo() {
		return termNo;
	}
	public void setTermNo(int termNo) {
		this.termNo = termNo;
	}
	public String getTerm() {
		return term;
	}
	public void setTerm(String term) {
		this.term = term;
	}
	
}
