package jxau.spms.common.po;

public class DepInfo {

	private String departNo;
	private String departName;
	private String departBrief;
	private String departInfo;
	private int depState;
	
	public String getDepartNo() {
		return departNo;
	}
	public void setDepartNo(String departNo) {
		this.departNo = departNo;
	}
	public String getDepartName() {
		return departName;
	}
	public void setDepartName(String departName) {
		this.departName = departName;
	}
	public String getDepartBrief() {
		return departBrief;
	}
	public void setDepartBrief(String departBrief) {
		this.departBrief = departBrief;
	}
	public String getDepartInfo() {
		return departInfo;
	}
	public void setDepartInfo(String departInfo) {
		this.departInfo = departInfo;
	}
	public int getDepState() {
		return depState;
	}
	public void setDepState(int depState) {
		this.depState = depState;
	}

}
