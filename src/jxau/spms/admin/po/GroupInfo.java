package jxau.spms.admin.po;

public class GroupInfo {

	private int groupNo;
	private String groupName;
	private String groupLeader;
	private String groSecretary;
	private String telephone;
	private String papLocation;
	private String adminNo;
	private String groupTerm;
	
	public int getGroupNo() {
		return groupNo;
	}
	public void setGroupNo(int groupNo) {
		this.groupNo = groupNo;
	}
	public String getGroupName() {
		return groupName;
	}
	public void setGroupName(String groupName) {
		this.groupName = groupName;
	}
	public String getGroupLeader() {
		return groupLeader;
	}
	public void setGroupLeader(String groupLeader) {
		this.groupLeader = groupLeader;
	}
	public String getGroSecretary() {
		return groSecretary;
	}
	public void setGroSecretary(String groSecretary) {
		this.groSecretary = groSecretary;
	}
	public String getTelephone() {
		return telephone;
	}
	public void setTelephone(String telephone) {
		this.telephone = telephone;
	}
	public String getPapLocation() {
		return papLocation;
	}
	public void setPapLocation(String papLocation) {
		this.papLocation = papLocation;
	}
	public String getAdminNo() {
		return adminNo;
	}
	public void setAdminNo(String adminNo) {
		this.adminNo = adminNo;
	}
	public String getGroupTerm() {
		return groupTerm;
	}
	public void setGroupTerm(String groupTerm) {
		this.groupTerm = groupTerm;
	}
	
}
