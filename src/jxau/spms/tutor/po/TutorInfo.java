package jxau.spms.tutor.po;

public class TutorInfo {

	private String tutorNo;
	private String tutorPass;
	private int tutorState;
	
	public String getTutorNo() {
		return tutorNo;
	}
	public void setTutorNo(String tutorNo) {
		this.tutorNo = tutorNo;
	}
	public String getTutorPass() {
		return tutorPass;
	}
	public void setTutorPass(String tutorPass) {
		this.tutorPass = tutorPass;
	}
	public int getTutorState() {
		return tutorState;
	}
	public void setTutorState(int tutorState) {
		this.tutorState = tutorState;
	}
}
